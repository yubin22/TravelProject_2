// In the following example, markers appear when the user clicks on the map.
// Each marker is labeled with a single alphabetical character.
const labels = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
let labelIndex = 0;

function initMap() {
  const korean = {lat: 37.56667, lng: 126.97806};
  const korean2 = {lat: 37.5115557, lng: 127.0595261};
  const map = new google.maps.Map(document.getElementById("map"), {
    zoom: 5,
    center: korean,
  });

  // This event listener calls addMarker() when the map is clicked.
  google.maps.event.addListener(map, "click", (event) => {
    addMarker(event.latLng, map);
  });
  // Add a marker at the center of the map.
  addMarker(korean, map);
  addMarker(korean2, map);
}

// Adds a marker to the map.
function addMarker(location, map) {
  // Add the marker at the clicked location, and add the next-available label
  // from the array of alphabetical characters.
  new google.maps.Marker({
    position: location,
    label: labels[labelIndex++ % labels.length],
    map: map,
  });

}

window.initMap = initMap;


function getCheckboxValue()  {
  // 선택된 목록 가져오기
  const query = 'input[name="cart"]:checked';
  const selectedEls = 
      document.querySelectorAll(query);
  
  // 선택된 목록에서 value 찾기
  let result = '';
  selectedEls.forEach((el) => {
    result += el.value + ' ';
  });
  
  // 출력
  document.getElementById('confirm').innerText
    = result;
}


